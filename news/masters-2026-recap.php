        <section>
            <h2>How It Unfolded</h2>

            <h3>Saturday: The Lead Evaporates</h3>
            <p>Augusta National yielded its lowest-ever field scoring average in a third round, and McIlroy had one of the worst days in the field. He made three bogeys and a double, offset by just four birdies, to sign for a 73 — a one-over round on a day when birdies were available across the course. Cameron Young took full advantage, carding a 65 to wipe out the entire six-shot cushion and pull level with McIlroy at 11-under heading into Sunday. What had felt like a coronation on Friday night became a wide-open major championship heading into the final day.</p>

            <h3>Sunday: The Comeback, Amen Corner, and the Finish</h3>
            <p>The final round shifted constantly. Young wrestled the lead away from McIlroy midway through the back nine. Justin Rose surged to briefly top the leaderboard. Scottie Scheffler, who else, made his customary charge to put himself within range. At various points on Sunday, it seemed like anyone could win it.</p>

            <p>Then came Amen Corner, and Rory McIlroy was himself again. Standing on the 12th tee with a one-shot lead and the full weight of Augusta's most daunting par-3 in front of him — Rae's Creek waiting to swallow anything short, the green narrow and deceptive — he made the shot. His tee shot came to rest inside seven feet of the hole. He made the birdie. He parred the 13th with another steady approach. When he reached the 14th tee, the tournament was his to lose again, and this time he didn't.</p>

            <p>The 18th hole provided one final scare when his tee shot found the trees, but he scrambled to make the par that sealed a one-shot victory at 12-under 276. When he walked off the green, both arms went to the sky — an instinctive, unscripted gesture that the gallery answered with the loudest roar Augusta had heard in years.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>Only the Fourth Player in History</h3>
            <p>The list of players who have won back-to-back Masters is three names long: Jack Nicklaus, who did it in 1965 and 1966; Nick Faldo, who repeated in 1989 and 1990; and Tiger Woods, who won in 2001 and 2002. Rory McIlroy is now the fourth. The significance of that is hard to overstate — decades pass between additions to that list, and the Masters demands a combination of consistent excellence and the mental fortitude to return to Augusta as the defending champion and do it again. McIlroy did exactly that, through one of the more turbulent 72-hole windows Augusta has produced in recent memory.</p>

            <h3>The Six-Shot Lead That Wasn't Safe</h3>
            <p>The 36-hole lead of six shots was the largest in Masters history at the midway point. By the time Saturday ended, it was gone entirely. What McIlroy went through during that Saturday collapse — watching the board shift, watching the lead shrink to nothing — and then returning Sunday to win by one shot says more about his character than any clean victory could. He had to absorb the pressure of giving it away and then go out and win it anyway. That is what separates the great ones.</p>

            <h3>Scheffler Comes Up One Short Again</h3>
            <p>Scottie Scheffler finished alone in second at 11-under, one stroke behind McIlroy. He had won this tournament in 2022 and 2024 and is by any measure one of Augusta National's most dangerous players. A runner-up finish at the 2026 Masters is not a failure — it is the natural result of going up against a champion who refused to give it back when it mattered most.</p>
        </section>

        <section>
            <h2>Final Thoughts</h2>
            <p>The 2026 Masters will be remembered for many things: the largest 36-hole lead in tournament history, the Saturday that nearly unraveled everything, Sunday's battle through Amen Corner, and the moment Rory McIlroy lifted his arms to the Augusta sky as a two-time, back-to-back champion. It was messy and brilliant and deeply human — exactly the kind of Masters that the sport produces when its biggest stage meets its most compelling players. McIlroy now has five major championships and a permanent place in the conversation about the game's all-time greats. He earned both of them at Augusta this April.</p>
