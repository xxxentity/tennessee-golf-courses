            <p>Port Royal, a public course perched along the Atlantic coastline, is known for its dramatic seaside holes and the unpredictable winds that sweep off the ocean. Sunday delivered those conditions in full — gusts topping 30 mph turned the final round into a test of survival as much as skill.</p>
