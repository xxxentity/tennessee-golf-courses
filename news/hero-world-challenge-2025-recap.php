        <section>
            <h2>How It Unfolded</h2>
            <p>Sepp Straka carried the 54-hole lead into Sunday, but Matsuyama made quick work of the deficit. He went out in 5-under 31 on the front nine and then holed out for eagle at the par-5 10th, a moment that swung the tournament firmly in his direction. He never looked back, completing a bogey-free 64 to post 22-under par and set the target.</p>

            <p>Alex Noren matched him shot for shot down the stretch, also signing for a 64 to force a playoff. The Swede had played quietly all week but saved his best for Sunday, giving himself a legitimate shot at the biggest win of his career.</p>

            <p>On the first playoff hole — the par-4 18th — Matsuyama put the round away. He struck a 9-iron from 166 yards to within three feet of the hole and calmly tapped in the birdie after Noren missed his attempt. The win was his, $1 million richer, and the perfect final chapter to a remarkable 2025 season.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>Matsuyama Bookends His Season</h3>
            <p>The Hero World Challenge victory gave Matsuyama two wins in the 2025 calendar year, a fitting bookend to a season that included one of the most celebrated titles in recent memory. His performance at Albany was a reminder that when Matsuyama is driving it well and his putter is on, he is one of the most dangerous players in the world. A bogey-free 64 in a final round when a title is on the line is not easy golf — he made it look like it was.</p>

            <h3>Scheffler's Three-Peat Falls Short</h3>
            <p>Scottie Scheffler came to Albany looking to win the Hero World Challenge for the third consecutive year, which would have been a remarkable achievement in an event this competitive. A closing 68 left him tied for fourth at 20-under, two shots out of the playoff. It wasn't the finish he was looking for, but in a year as dominant as Scheffler's 2025, the loss at Albany is a footnote rather than a headline.</p>

            <h3>Noren's Near-Miss</h3>
            <p>Alex Noren's runner-up finish deserves recognition. The Swedish veteran matched the best round of the day in the final round and pushed Matsuyama all the way to the playoff before the champion's precision on the 18th hole ended it. For Noren, a player who has had moments of brilliance throughout his career, Sunday at Albany was a reminder of what he is still capable of at the highest level.</p>
        </section>

