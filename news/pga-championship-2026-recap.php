            <p>Twenty-two players stood within four shots of the lead heading into Sunday at Aronimink Golf Club. Jon Rahm was there. Rory McIlroy was there. Scottie Scheffler, who had won this championship twice before, was five back and said publicly that anyone could win it. He was right — it just wasn't anyone the world had been watching. Aaron Rai, 31 years old, ranked 44th in the world, wearing two gloves and iron covers on his irons the way he has since childhood, shot 65 in the final round of the 108th PGA Championship and walked away with the Wanamaker Trophy by three shots. He is the first Englishman to win this tournament since 1919. He is the first player of Indian descent to win a men's major championship. He is, improbably and undeniably, the 2026 PGA Champion.</p>
