            <h2>How It Unfolded</h2>
            <p>The final round at Pebble Beach was a moving leaderboard throughout, and the drama peaked at the par-5 18th. Sepp Straka holed out for eagle on the closing hole to vault to 21-under and share the lead with Min Woo Lee, turning what had been Morikawa's advantage into a sudden three-way battle with the final group still on the course.</p>

            <p>Morikawa answered with the shot of the tournament. Needing birdie on 18 to win outright, he stood 235 yards from the flag with the Pacific Ocean lurking to the left. He flushed a 4-iron, starting the ball out over the ocean wall and trusting the coastal wind to bring it back — a shot that requires precision, nerve, and a complete understanding of the conditions. The ball landed on the green and set up a makeable birdie putt. He converted, signed for a 67, and walked off Pebble Beach as the champion at 22-under, one clear of Straka and Lee.</p>

            <p>Scottie Scheffler's closing 63 was the low round of the day and vaulted him into a share of fourth, but he had too much ground to make up on the leaders.</p>
