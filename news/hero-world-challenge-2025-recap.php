            <p>Scottie Scheffler entered the week as the two-time defending champion and the heavy favorite to claim his third straight title. Instead, it was Matsuyama who seized the moment — overtaking 54-hole leader Sepp Straka with a brilliant closing round that included an eagle at the par-5 10th and left the field scrambling to keep pace.</p>
