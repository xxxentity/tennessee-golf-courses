            <p>Joaquin Niemann had gone winless through the first seven events of the 2026 LIV Golf season — a stretch that felt long for a player who had won five times in 2025. On Sunday in Busan, he made up for all of it. The 27-year-old Chilean birdied the first playoff hole to defeat Talor Gooch at Asiad Country Club and claim his record eighth career LIV Golf individual title, more than any other player in the league's history.</p>
