            <p>Four days and 72 holes were not enough. A Sunday weather delay pushed the finish to Monday morning, and Viktor Hovland and Scottie Scheffler returned to TPC River Highlands tied at 21 under to settle the 2026 Travelers Championship in a sudden-death playoff. One hole was all it took. Hovland's birdie putt crept in on the right side of the 18th cup. Scheffler's birdie attempt did not. Hovland is the champion — his first PGA Tour win in 15 months, earned at the end of one of the more dramatic finishes the Signature Event schedule has produced this season.</p>
