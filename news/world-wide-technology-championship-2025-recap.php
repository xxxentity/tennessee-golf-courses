            <h2>Tournament Overview</h2>
            <p>The World Wide Technology Championship, held November 6-9, 2025, at El Cardonal at Diamante in Los Cabos, Mexico, saw Griffin rally from behind to claim his third PGA Tour title of the season. Playing in one of golf's most scenic settings along the Pacific coastline of Baja California, Griffin put together four rounds of exceptional golf — capped by a breathtaking Sunday closing round that left the field in his wake.</p>

            <p>Griffin's winning total of 29-under par 259 broke the tournament scoring record, and his two-shot margin of victory over a tight leaderboard understates just how commanding his final round was. Entering Sunday not leading, he overtook overnight leaders Garrick Higgo and Carson Young with a five-birdie streak that was simply unstoppable.</p>
