            <h2>How It Unfolded</h2>
            <p>Heading into the final holes of regulation, Matsuyama held the advantage at 17-under — one clear of Gotterup and seemingly in firm control. He birdied the par-5 15th to extend his lead, then parred 16 and 17. Standing on the 18th tee needing only a par to win, the title appeared to be his.</p>

            <p>It wasn't. Matsuyama's driver had been causing trouble all week, and it betrayed him again at the worst possible moment. A pulled tee shot found the bunker, and after his second shot struck the lip of the bunker, he was unable to get up-and-down. The closing bogey dropped him back to 16-under and into a tie with Gotterup, who had birdied five of his last six holes to force the playoff.</p>

            <p>The sudden-death playoff returned to the 18th, and the drama continued. Gotterup striped a 360-yard drive down the middle. Matsuyama again struggled off the tee — his drive struck a steel pole along the edge of the course and caromed directly into the water hazard. From there, recovery was impossible. Gotterup needed only to make birdie, and he did, draining a 27-foot putt to seal one of the wilder finishes TPC Scottsdale has hosted in recent memory.</p>
