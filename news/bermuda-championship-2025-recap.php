        <section>
            <h2>How It Unfolded</h2>
            <p>Schenk built his position over three solid rounds — a 69 to open, followed by a 65 that moved him into contention, and a 67 on Saturday to put himself near the top of the leaderboard heading into the final day. He wasn't the flashiest name on the board, but he was consistent, and in Bermuda's conditions, consistency is everything.</p>

            <p>Sunday was a different kind of test. The wind arrived early and didn't let up, with gusts cresting 30 mph and turning Port Royal into one of the toughest tracks on Tour. Schenk played a steady, grinding round — one birdie, four crucial par saves, and a final-hole moment that will stay with him forever.</p>

            <p>Standing on the 18th green, clinging to a one-shot lead, Schenk's approach ran just over the green. He reached for his putter to play through roughly six feet of fringe. The wind pushed his ball up short of the hole, leaving him a five-footer for par. He stepped up and rolled it in. Victory in his 243rd start on Tour.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>A Long Road to the Winner's Circle</h3>
            <p>The most compelling part of Schenk's win wasn't the golf — it was the journey. The Indiana native had been a fixture on Tour for years without a breakthrough. He had come close twice in 2023, finishing runner-up on two occasions, and even reached the Tour Championship that year. But the win never came.</p>

            <p>This season had been particularly rough. Schenk missed six consecutive cuts during the summer, and his FedExCup standing fell to 134th — right on the edge of losing his Tour card. He made changes to his game and, notably, switched to a one-handed putting grip to address what he identified as his biggest weakness. When asked about the win afterward, he described it simply as a surreal moment.</p>

            <h3>Chandler Phillips Falls One Short</h3>
            <p>Chandler Phillips played a strong final round of his own, also carding a 71 in the difficult conditions, but it wasn't quite enough. He finished at 11-under, one stroke back, watching Schenk hold on from the final green. Phillips' runner-up was a solid result that continued to show his development as a Tour-level player.</p>

            <h3>Port Royal's Wind Levels the Field</h3>
            <p>The five players tied for third at 10-under — Smalley, Whaley, Capan, Kanaya, and McGreevy — all illustrated how brutally the wind affected scoring on Sunday. A four-round total that might not win in calmer conditions proved more than competitive at Port Royal. The course's Atlantic exposure and the seasonal conditions made scoring a premium all week, and Sunday's wind made even par feel like an accomplishment.</p>
        </section>

