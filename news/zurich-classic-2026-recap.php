            <h2>How It Unfolded</h2>
            <p>The Fitzpatricks built their lead through three rounds and carried it into Sunday's alternate-shot final with the tournament in their hands. But the closing holes at TPC Louisiana tightened things considerably. Alex Smalley and Hayden Springer made a run, and Kristoffer Reitan and Kris Ventura produced a stunning final-round performance — three eagles on their scorecard and a 65 that briefly threatened to change the outcome. When Smalley and Springer narrowly missed a putt that would have given them outright possession of the lead, both teams sat at 30-under and the pressure shifted squarely onto the Fitzpatricks.</p>

            <p>Matt and Alex made birdie on the 72nd hole to reach 31-under and win by one. The manner of it — Matt's bunker shot to a foot, Alex's tap-in to seal it — was as clean a closing sequence as you could draw up. When the ball fell in, both brothers knew exactly what it meant.</p>

            <p>Rory McIlroy, watching from elsewhere, reportedly sent Matt a five-word expletive text after seeing the bunker shot. The golf world understood the feeling.</p>
