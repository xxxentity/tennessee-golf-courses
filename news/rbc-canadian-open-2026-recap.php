            <h2>Tournament Overview</h2>
            <p>The 2026 RBC Canadian Open was played June 11 through 14 at TPC Toronto at Osprey Valley's North Course in Caledon, Ontario, roughly an hour northwest of downtown Toronto. The North Course, a par-70 layout stretching to 7,389 yards, underwent a significant renovation in 2023 and 2024 under architect Ian Andrew in preparation for the return of PGA Tour-caliber competition. Andrew sharpened the course's teeth: opening views between holes through strategic tree removal, lengthening the total yardage, tightening fairways in the landing zones where Tour players drive the ball, and adding chipping areas around greens that were already among the most demanding on the schedule. The result is a par 70 with two par 5s, four par 3s, and twelve par 4s — a layout whose primary defense is its putting surfaces and whose finishing hole, a par 5 that requires both a demanding tee shot and an approach over water, has the ability to end a tournament or flip one entirely.</p>

            <p>The field arrived for the final event in which players could earn Aon Swing 5 points toward FedExCup qualification, giving the week an added layer of consequence for players on the bubble. Among the notable names in the field were Matt Fitzpatrick, Viktor Hovland, Tommy Fleetwood, Wyndham Clark, Brooks Koepka, Sam Burns, and defending champion Ryan Fox — all of them chasing a title and the world ranking points that come with it. What none of them could plan for was what Bud Cauley had in his bag for four days.</p>
        </section>

