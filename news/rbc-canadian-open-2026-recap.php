            <h2>How It Unfolded</h2>

            <h3>Round 1: A Six-Way Jam at the Top</h3>
            <p>Thursday's opening round at TPC Toronto produced a crowded leaderboard at the top. Six players finished the day at six under par — Sahith Theegala, Matthew Anderson, Emiliano Grillo, Eric Cole, Brooks Koepka, and Sam Burns — sharing the first-round lead after shooting 64 on a course that was gettable in the conditions but far from easy. Bud Cauley was not among them. He signed for a one-under 69 — steady, unspectacular, well within himself — and went to bed five shots off the lead. On a par 70, five shots is not nothing. It is, however, the kind of gap that disappears with one big round.</p>

            <h3>Round 2: Cauley Launches, James Leads</h3>
            <p>Friday was Cauley's day. He went out and shot a seven-under 63 — the low round of the tournament — that moved him to eight under through 36 holes and into serious contention. The round was a statement: birdies kept coming, nothing got away from him, and the course that had resisted on Thursday gave him nothing but green lights. He was not alone at the top, however. Benjamin James, a 23-year-old who had done little to announce himself to this field before the week began, eagled his opening hole and posted a round good enough to take the solo lead at 10 under through two rounds. James's name was not one anyone had circled in their preview — which made his position at the halfway mark all the more striking. Cauley was two shots back heading into the weekend.</p>

            <h3>Round 3: Suber Seizes Moving Day</h3>
            <p>Saturday reshuffled the leaderboard in a way that set up a wide-open Sunday. Jackson Suber, who had been lurking throughout the week, played the best ball-striking round of his tournament and grabbed the solo lead at 13 under par. Cauley sat one shot behind at 12 under, his round of 66 keeping him in the pack without separating from it. Wyndham Clark, Tommy Fleetwood, Brice Garnett, and Jesper Svensson clustered at 11 under, with Billy Horschel, Ryan Fox, Jimmy Stanger, and Sam Burns all within three shots of the lead at 10 under. Matt Fitzpatrick and Viktor Hovland — both ranked inside the top 20 in the world — sat at nine under, needing big final rounds to have any chance. The leaderboard entering Sunday was one of the most compressed of the PGA Tour season: eight players within four shots of the lead, and a course whose finish could produce anything.</p>

            <h3>Final Round: Cauley Closes the Door</h3>
            <p>Sunday at TPC Toronto sorted itself quickly and then held its shape. Cauley birdied the par-4 12th with a chip-in that moved him ahead of Suber and gave him the outright lead he never relinquished. He played the back nine with the kind of composure that suggests a player who has been through enough to know that a Sunday lead at a golf tournament is not the scariest thing life can ask of you. He signed for a five-under 65 — four rounds of 69-63-66-65 — and posted 17 under at the clubhouse.</p>

            <p>Matt Fitzpatrick made the most serious charge. The Englishman shot a six-under 64 in the final round to move from nine under all the way to 15 under, a run that gave the grandstand a genuine contest for most of the afternoon. But Cauley held a two-shot advantage when the last putt dropped, and Fitzpatrick had to settle for solo second. Viktor Hovland finished one further back in third at 14 under. Suber, who had led entering Sunday, fell back in the group tied for fourth alongside Stanger, Garnett, and Svensson. The win belonged entirely to Cauley.</p>
