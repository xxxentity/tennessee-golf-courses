        <section>
            <h2>Key Storylines</h2>

            <h3>Breaking Tiger's Record at Torrey Pines</h3>
            <p>The 23-under total wasn't just a winning score — it was a tournament record, surpassing the previous mark of 22-under set by Tiger Woods in 1999. Torrey Pines is one of Woods' most hallowed venues, the site of his 2008 U.S. Open victory and multiple Tour wins, so eclipsing his scoring record there carries extra weight. Rose erased it with room to spare, leaving his name alone atop the all-time leaderboard at one of the most famous courses in American golf.</p>

            <h3>The First Wire-to-Wire Win at Torrey Pines in 71 Years</h3>
            <p>Rose became the first player to lead the Farmers Insurance Open wire-to-wire in 71 years — a remarkable distinction at a tournament with as much history as this one. Holding the lead for four full rounds at any Tour event requires nerve, consistency, and the ability to handle the pressure of the target on your back. Rose made it look effortless, extending his advantage at every turn rather than holding on for dear life.</p>

            <h3>A 45-Year-Old Rewrites the Record Books</h3>
            <p>The age story is impossible to ignore. At 45, Rose is not supposed to be setting tournament scoring records and winning by seven shots on one of the toughest courses in professional golf. He went winless from 2020 through 2022, and many observers wondered whether the best of Justin Rose was behind him. He responded by winning in three of his last four PGA Tour seasons and collecting his 13th career Tour title at Torrey Pines — a venue he also conquered back in 2019. "Those days, those weeks, those rounds are definitely showing me the gas is still in the tank," he said after the round. Based on what he showed at Torrey Pines, the tank appears far from empty.</p>
        </section>

