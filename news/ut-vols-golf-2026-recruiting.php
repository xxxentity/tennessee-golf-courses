        <div class="article-intro">
            <p>Tennessee men's golf head coach Brennan Webb announced the signing of two players on Wednesday — Tyler Watts and Pennson Badgett — giving the Vols a 2026 recruiting class ranked third in the country by College Golf Commits. Watts enters as the No. 1 ranked junior golfer in the world, while Badgett checks in at No. 6. It is one of the strongest two-player classes Tennessee has ever landed.</p>
        </div>

        <section>
            <h2>Tyler Watts</h2>
            <p>Tyler Watts, a homeschooled golfer from Huntsville, Alabama, arrives at Tennessee as the top-ranked recruit in the 2026 class by College Golf Commits and the No. 1 ranked junior golfer in the world according to the World Amateur Golf Rankings. His junior résumé backs it up.</p>

            <p>Watts won the 2025 Sunnehanna Amateur Championship as the youngest winner in the storied event's history, a victory that capped his Elite Amateur Series Championship for the year. In 2023 he won the Alabama State Amateur, becoming the youngest champion in that tournament's 107-year history. He is a two-time AJGA First Team All-American (2023 and 2024) and a two-time member of the USGA's National Junior Team, one of 12 boys selected annually for the program. Additional wins include the Jones Cup Junior Invitational and the Southern Junior.</p>

            <p>In September 2025, Watts made his PGA Tour debut at the Procore Championship at Silverado Resort in Napa, California — and made the cut, finishing 3-under par. He and Badgett were both members of the 2024 Junior Presidents Cup Team.</p>
        </section>

        <section>
            <h2>Pennson Badgett</h2>
            <p>Pennson Badgett comes to Tennessee from Pilot Mountain, North Carolina, ranked sixth in the 2026 class by College Golf Commits and eighth on Junior Golf Scoreboard. The 2024 Rolex Junior All-American has accumulated three AJGA wins — at the AJGA Goldsboro Junior, the AJGA Adam Scott Junior Championship in Lake Worth, Florida (by three strokes), and the RLX Ralph Lauren Junior Classic, a 32-player match play event held at Bethpage Black. He also won the AJGA Polo Championship.</p>

            <p>Badgett has recorded top-five finishes at the Boys' Junior PGA Championship, the Scott Robertson Memorial, and the Wyndham AJGA Championship. He made match play at the 2025 U.S. Amateur, a further indication of his ability to compete at the highest amateur level. Like Watts, he was a member of the 2024 Junior Presidents Cup Team, giving Tennessee two players from that same U.S. squad arriving in the same class.</p>
        </section>

        <section>
            <h2>Webb's Program in Context</h2>
            <p>The 2026 class arrives at a program that has been building steadily under Webb, now in his seventh season at Tennessee. He ranks tied for third in program history for career tournament wins among Tennessee head coaches and has led the Vols to 11 team tournament titles and three NCAA Championships berths — in 2021, 2024, and 2025. He is the only coach in program history to record three triple-win seasons.</p>

            <p>Adding the top-ranked junior in the world alongside a top-ten prospect gives Webb and Tennessee a foundation to build toward competing for SEC titles and deep NCAA tournament runs in the coming years. Watts and Badgett are expected to join the program ahead of the 2025-26 academic year.</p>
