            <h2>Key Storylines</h2>

            <h3>The First Brothers to Win on the PGA Tour</h3>
            <p>Professional golf has seen a great deal in its history, but never this: two brothers winning a PGA Tour event together. Matt and Alex Fitzpatrick — Sheffield born, Hallamshire Golf Club raised — put their names in the record books at TPC Louisiana as the first siblings ever to win on the circuit. The Zurich Classic's team format gave them the opportunity; their complementary games and shared coach gave them the foundation to seize it.</p>

            <h3>Matt's Third Win of 2026</h3>
            <p>For Matt Fitzpatrick, the Zurich Classic completed a remarkable three-win spring. Following his Valspar Championship title and the RBC Heritage playoff victory over Scottie Scheffler, he became the first Englishman to win three or more times in a single PGA Tour season. He moved to the top of the 2026 FedExCup standings and cemented a case for being the hottest player in the world over a three-month stretch. His 4-iron at Harbour Town the week before was the shot of the RBC Heritage; his bunker shot at TPC Louisiana may have been the shot of the Zurich Classic.</p>

            <h3>Alex's Journey to the Tour Card</h3>
            <p>The most emotional dimension of the Zurich Classic win belonged to Alex. The 27-year-old younger brother had spent his career on the DP World Tour, where his trajectory had gone sideways just months earlier — six missed cuts in nine starts had dropped him outside the top 280 on the rankings, and his tour card was in jeopardy. The turning point came when he changed swing coaches, moving to Mike Walker — the same coach who had shaped Matt's game — and began to rebuild. The results came quickly: a jump from 133rd to 22nd in driving accuracy on tour, a first DP World Tour title at the Hero Indian Open, and then a ticket to New Orleans alongside his brother.</p>

            <p>When Alex tapped in the winning putt on the 18th green at TPC Louisiana, he earned a two-year PGA Tour exemption through the 2028 season. He officially accepted Tour membership days later and called the week a whirlwind. That might be the understatement of the season.</p>

            <h3>A Tournament-Record Total</h3>
            <p>The Fitzpatricks' winning score of 31-under 257 set a new tournament record at the Zurich Classic of New Orleans. With Reitan and Ventura posting three eagles in the final round and Smalley and Springer pressing until the final hole, the competition demanded a record to win it — and Matt and Alex delivered exactly that.</p>
        </section>

