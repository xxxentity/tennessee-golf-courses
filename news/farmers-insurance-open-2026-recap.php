            <h2>How It Unfolded</h2>
            <p>Rose's opening 62 on the North Course was the kind of round that announces a player is locked in from hole one. He led by one after day one, but his advantage ballooned from there — one shot after Round 1, four after Round 2, six after Round 3, and seven by the time he signed his final card Sunday. A closing 70 on the demanding South Course was all he needed, his lead large enough to absorb any adversity the final round threw at him.</p>

            <p>The field simply had no answer. Ryo Hisatsune, Si Woo Kim, and Pierceson Coody shared second place at 16-under — a full seven shots back. The margin wasn't just a win; it was a statement.</p>
