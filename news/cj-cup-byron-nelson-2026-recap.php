            <h2>How It Unfolded</h2>

            <h3>Kim's Week: Historic Birdie-Making, Dallas Crowd Behind Him</h3>
            <p>Si Woo Kim had been the story of the week from the moment he teed it up on Thursday. Through two rounds he had made so many birdies — flirting with 59 during a Friday round that had TPC Craig Ranch buzzing — that he held a five-shot lead at one point heading into the weekend. Saturday brought a slower round of 68, but he still led by two at 21 under, with 26 total birdies through 54 holes, one short of the Tour record for a single-tournament 54-hole stretch. Playing near his home in the Dallas area, with a crowd pulling for him, the setup for Sunday could not have been more favorable.</p>

            <h3>Clark Seizes the Day: Eagle at 12, Fire on the Back Nine</h3>
            <p>Clark began the final round tied with Scheffler, two behind Kim, and played a steady front nine without doing anything remarkable. Then came the par-5 12th. Clark hit a 4-iron from 246 yards to 16 feet and drained the eagle putt — turning a one-shot deficit into a one-shot lead in a single swing. It was the decisive moment of the tournament. The back nine that followed was extraordinary: Clark made five more birdies after the eagle, including a 45-foot putt on the par-3 15th that drew an emphatic fist pump, and another at the 17th stadium hole. He played the back nine in 28 strokes. By the time he finished, the 60 on his card was the lowest final round by any winner in the 2026 PGA Tour season.</p>

            <p>The putting numbers bordered on absurd: 158 feet of putts made in the final round alone, 112 feet per round for the week, first in strokes gained/putting for the tournament by a margin that suggested a different game than the one everyone else was playing.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>First PGA Tour Player to Win Twice With a Closing 60</h3>
            <p>Clark shot 60 in the final round of the 2024 AT&T Pebble Beach Pro-Am to win that shortened event. He shot 60 here. No player in PGA Tour history had previously won two tournaments with a closing 60. David Duval and Stuart Appleby are the only players to have won with a final-round 59, so Clark sits alone in a very specific part of the Tour's record books — the player who has made a habit of Sunday 60s when the trophy is on the line.</p>

            <h3>First Full-Event Win Since the 2023 U.S. Open</h3>
            <p>Clark's last full-field, 72-hole victory was the 2023 U.S. Open at Los Angeles Country Club, where he beat Rory McIlroy on the final day in one of the more dramatic U.S. Open Sundays in recent memory. The 2024 Pebble Beach win came in a shortened tournament. This was the result his game had been pointing toward for stretches of the 2025 and 2026 seasons — and the putter made it happen. Clark had been ranked 132nd in strokes gained/putting entering the week, a number that bore no resemblance to what he produced at TPC Craig Ranch. He had switched to a Ping Scottsdale Tec Ally Blue mallet earlier this spring, and the decision paid off in dramatic fashion.</p>

            <h3>Scheffler's Winless Stretch Continues</h3>
            <p>Scottie Scheffler finished third at 25 under — his tenth consecutive tournament without a win since his American Express victory in January. He had briefly been in contention through the middle of the final round but couldn't generate the birdies to keep pace with Clark's extraordinary back nine. Two shots behind Kim and Clark at the sixth hole, his second shot hit the pin and spun away 54 feet; he made par on a hole both leaders birdied. In a week where a closing 60 was required to win, Scheffler's inability to match that pace said more about Clark than it did about the world's top-ranked player.</p>

            <h3>Kim's Near-Historic Week Goes Without a Title</h3>
            <p>The most bittersweet result of the week belonged to Kim. His 26 birdies through 54 holes were one short of the Tour record, his three-day command of the leaderboard was real and earned, and his finish at 27 under would have been a winning score in nearly any other version of this tournament. Clark simply played a better final day. Kim took his runner-up finish like the competitor he is — this was not a collapse, not a choke, just a week where someone else happened to shoot 60.</p>
        </section>

        <section>
            <h2>Final Thoughts</h2>
            <p>The 2026 CJ Cup Byron Nelson will be remembered for one number: 60. Wyndham Clark took what looked like Si Woo Kim's tournament to lose and turned it into a showcase for the most electric final round of the PGA Tour season, anchored by an eagle at 12, a 45-foot birdie at 15, and a back-nine 28 that pushed the whole field aside. Clark is a two-time winner with two closing 60s, a U.S. Open champion who just proved he can do it in full-field 72-hole competition again. The leaderboard in Texas on Sunday afternoon was history-grade. Clark made sure the right name ended up on top of it.</p>
