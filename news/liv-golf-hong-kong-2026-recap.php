            <h2>How It Unfolded</h2>
            <p>Rahm, Thomas Detry, and Harold Varner III shared the 54-hole lead entering Sunday, setting up a three-way battle that never got particularly close. Rahm came out firing, reeling off birdies with the kind of controlled aggression that made him one of the best players in the world. Eight birdies in a 64 left the chasing pack with no chance — Detry finished second at 20-under, three shots back, and Pieters was another stroke behind in third.</p>

            <p>After the round, Rahm was candid about what had changed. "I think I was a little tentative at Adelaide," he said, referencing the second-place finish where he and Bryson DeChambeau had led by five entering the final round before Anthony Kim's stunning 63 overtook them both. "Once I made that putt on 13 today, my mindset was to birdie them all coming in." He nearly did.</p>
