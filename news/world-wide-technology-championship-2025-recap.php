        <section>
            <h2>Griffin's Unstoppable Sunday</h2>
            <p>For three rounds, the tournament belonged to anyone's guess. Garrick Higgo and Carson Young shared the 54-hole lead heading into Sunday, with several players within striking range. Ben Griffin was lurking, but few expected what was coming.</p>

            <p>Griffin caught fire at the eighth hole and did not cool down. He rattled off five consecutive birdies from holes 8 through 12, a stretch of golf that swung the tournament firmly in his direction. The run included three lengthy putts that showcased the precision with his putter he had found for the week — a 40-foot conversion on the par-3 eleventh, a 25-footer on the twelfth that gave him his first lead of the day, and a 25-foot make on the par-3 sixteenth that all but ended the contest.</p>

            <p>When the final putt dropped, Griffin had carded 10 birdies against a single bogey for a round of 63, producing a tournament record total of 29-under par. Sami Valimaki of Finland made a run with his own 64, and Chad Ramey posted 65, but neither could match Griffin's pace. The two-shot winning margin was comfortable by the end, though it had not felt that way for much of the afternoon.</p>
        </section>

        <section>
            <h2>Historic Company</h2>
            <p>With the win, Ben Griffin joined Scottie Scheffler and Rory McIlroy as the only PGA Tour players to claim three victories during the 2025 season. That is rarefied company — Scheffler and McIlroy are two of the best players in the world, and for a 29-year-old Griffin to keep pace with them in terms of win totals speaks to the year he had put together.</p>

            <p>The victory followed his Charles Schwab Challenge win in the spring and the Zurich Classic team title alongside partner Andrew Novak. But this felt different — a solo title, in a final-round chase, producing a record-setting total on one of the Tour's most scenic stops.</p>

            <p>Griffin jumped to a career-high No. 9 in the Official World Golf Ranking following the win, a significant milestone for a player who had steadily built his game into one of the Tour's most consistent over the past two seasons.</p>
        </section>

        <section>
            <h2>The Venue: El Cardonal at Diamante</h2>
            <p>The World Wide Technology Championship has built its identity around spectacular golf settings, and El Cardonal at Diamante in Los Cabos delivers on that promise. The course, designed by Tiger Woods, stretches along the natural terrain of Baja California with sweeping Pacific Ocean views. Low scoring is achievable when the wind cooperates, and the 2025 field took full advantage of the conditions throughout the week — as evidenced by Griffin's record-setting aggregate.</p>
        </section>

        <section>
            <h2>Final Thoughts</h2>
            <p>The 2025 World Wide Technology Championship will be remembered for Ben Griffin's brilliant Sunday charge and the tournament record he set in the process. Firing a 63 in a final round to win by two and produce the lowest 72-hole total in tournament history is a performance that earns its place among the most impressive victories of the 2025 PGA Tour season. Griffin came into the week as a credible contender and left as a record-setter, continuing what had already been a career year by any measure.</p>
