    <div class="article-content">
        <div class="article-intro">
            <p>The Tennessee Golf Foundation announced Friday that Mike Eller, PGA and Bob Wolcott, PGA have been selected as the 2026 inductees into the Tennessee Golf Hall of Fame. The two men will become the 59th and 60th members enshrined in the Hall, joining a class of Tennessee's most celebrated players, instructors, and contributors to the game.</p>
        </div>

        <section>
            <h2>Mike Eller, PGA</h2>
            <p>Mike Eller's connection to Tennessee golf runs deep. He co-founded the original 18 holes at Hermitage Golf Course — the General's Retreat course — which opened in 1986 in Old Hickory just outside Nashville. The course went on to host the LPGA Tour's Sara Lee Classic for 12 consecutive years, from 1988 through 1999, putting Tennessee on the national women's professional golf map during that stretch. In 2012, Eller purchased his partner's share and became the sole owner of Hermitage Golf Properties, Inc., where he has continued to operate the facility.</p>

            <p>Eller's career has encompassed more than course ownership. He served as Chapter President and Board member within the PGA of America's Tennessee Section, and as past Tennessee Section President. In 2007, the PGA honored him with its Distinguished Career Award. A decade and a half earlier, in 1993, the National Golf Foundation awarded the Eller family the Jack Nicklaus Award — recognizing the family's broad involvement across club professional roles, course ownership, PGA board service, and tournament competition.</p>

            <p>The Tennessee Golf Foundation described Eller as representing the best of Tennessee golf — a champion in competition and a leader in service, whose career reflects both dedication to personal achievement and a lasting commitment to growing the game through mentorship and stewardship.</p>
        </section>

        <section>
            <h2>Bob Wolcott, PGA</h2>
            <p>Bob Wolcott built his reputation as one of the most accomplished competitive golfers to come out of Tennessee. A three-time All-SEC first team All-American during his college career, Wolcott went on to play on the PGA Tour from 1989 through 1992 and again in 1997, recording two third-place finishes during his time on the circuit. His amateur and regional competitive record is equally strong: he won the Tennessee Amateur and Open, along with the Missouri and Arkansas State Open Championships twice each.</p>

            <p>After his playing career at the highest level, Wolcott transitioned into club golf, serving as Director of Golf at GreyStone Golf Club. He also captured the 2013 Tennessee Senior State Open, demonstrating that his competitive game remained sharp well beyond his Tour years. His son Hunter Wolcott has continued the family's tradition on professional tours, winning the Diners Club Peru Open on the PGA Tour Americas in May 2025 for his first professional victory.</p>

            <p>Like Eller, the Tennessee Golf Foundation cited Wolcott's commitment to mentorship and the long-term health of the game in Tennessee alongside his competitive achievements as the basis for his selection.</p>
        </section>

        <section>
            <h2>About the Tennessee Golf Hall of Fame</h2>
            <p>The Tennessee Golf Hall of Fame is administered by the Tennessee Golf Foundation and recognizes individuals who have made exceptional contributions to the game of golf in the state — whether through competitive achievement, instruction, administration, or service. Eller and Wolcott will bring the Hall's total membership to 60 when they are formally inducted at a ceremony in 2026, with details still to be finalized. The Hall includes players, teaching professionals, administrators, and contributors who have shaped the game's presence in Tennessee across more than a century of organized golf in the state.</p>
