        <div class="article-intro">
            <p>Si Woo Kim had led this tournament for three days. He had 26 birdies through 54 holes — one short of the Tour's all-time record for a 54-hole stretch — and a two-shot cushion going into Sunday at TPC Craig Ranch, playing near his adopted home in the Dallas area. The tournament looked like his to lose. And then Wyndham Clark shot 60. Clark made nine birdies and an eagle, put up a back-nine 28, and overtook Kim by three shots to win the 2026 CJ Cup Byron Nelson — the first PGA Tour player to win twice with a closing 60, and his first full-event Tour win since the 2023 U.S. Open.</p>
