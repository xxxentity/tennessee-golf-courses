            <h2>Tournament Overview</h2>
            <p>The 108th PGA Championship, held May 14-17 at Aronimink Golf Club in Newtown Square, Pennsylvania, marked the first major championship at this venue since Gary Player won the PGA Championship here in 1962. The Donald Ross design sits 13 miles west of Philadelphia in the western suburbs, and it plays nothing like the modern courses that populate the PGA Tour calendar. The transition from fairway to rough is immediate — no forgiving intermediate cut, just tight grass giving way to deep fescue — and the slick, well-defended greens punish imprecision on approach. It was always going to reward a specific kind of player.</p>

            <p>Through three rounds, 22 players had finished within four shots of the top, a PGA Championship record for 54-hole congestion. It set up the most chaotic major final round in recent memory — and produced one of its most surprising champions.</p>
