            <h2>How It Unfolded</h2>
            <p>Riley came into Sunday with every reason for confidence. He held a two-shot cushion and had played some of the cleanest golf of the week through the first three rounds. But the back nine at Waialae had other ideas.</p>

            <p>Riley's round unraveled quickly on the front nine. Three-putt bogeys from long range on the sixth and seventh holes bled momentum, and then a wild tee shot into the trees on the eighth led to a double bogey that swung the tournament entirely. In the space of a few holes, Riley went from two ahead to three behind — and he never recovered.</p>

            <p>Gotterup, meanwhile, was doing what he does best: making putts when it matters. He drained a 20-footer on the 12th, then followed it with a 25-foot conversion on the 13th — the hardest hole on the course — to put himself firmly in command. A perfectly placed tee shot on the par-3 17th set up a final birdie, and Gotterup signed for a 64 that left no doubt. Ryan Gerard made a late run with birdies on his final two holes to finish solo second at 14-under, but the gap was too large to close.</p>
        </section>

