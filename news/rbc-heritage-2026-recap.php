            <h2>How It Unfolded</h2>

            <h3>Fitzpatrick's Lead Slips Away</h3>
            <p>Through the first 13 holes of Sunday's final round, Fitzpatrick was grinding steadily — birdies at holes one and three had extended his lead to four, and 14 consecutive pars had kept the field at bay. But with four holes to play, Scheffler made his move. The world No. 1 birdied 15, 16, and 17 in rapid succession — three straight birdies on Harbour Town's closing stretch — to pull level and force Fitzpatrick's hand at the last.</p>

            <p>Fitzpatrick had a chance to win it in regulation but hit a poor chip from right of the green on 18, then missed the 20-foot par putt coming back. The bogey dropped him to 18-under, exactly where Scheffler sat after his remarkable comeback. A playoff it was.</p>

            <h3>The Playoff — One of Harbour Town's Best</h3>
            <p>The gallery at the 18th had been loud all week, and for the playoff hole it turned up another level entirely. As Fitzpatrick stood over his approach shot, chants of "U-S-A!" echoed around the Calibogue Sound. He stepped away once, gathered himself, and then hit what he later described as a little more left than intended but no less magnificent — a 4-iron that landed on the green, rolled past the pin, and came to rest 13 feet from the hole.</p>

            <p>Scheffler's response with a 6-iron was badly mishit, coming up 37 yards short of the hole. It was the worst swing he made all week, at the worst possible moment. Fitzpatrick rolled in the 13-footer for birdie to win the tournament for the second time and silence the partisan crowd in the best way a visiting player can — with the scoreboard.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>A Course He Has Always Called His Own</h3>
            <p>Fitzpatrick has a genuine personal connection to Harbour Town that goes back well before he ever competed there professionally. His family used to take holidays to Hilton Head Island when he was a boy — the golf, the tennis, the beaches. He came to this tournament as a kid and thought it would be something to win it one day. He has now done it twice, in 2023 (in a playoff over Jordan Spieth) and again in 2026, and the pattern of his performance here suggests this course fits him as well as any on the schedule.</p>

            <h3>Second Win of the Season, Fourth Career Title</h3>
            <p>The RBC Heritage was Fitzpatrick's second win of the 2026 PGA Tour season, following the Valspar Championship in mid-March. His four career PGA Tour titles — the 2022 U.S. Open at The Country Club, the 2023 RBC Heritage, the 2026 Valspar, and now his second Harbour Town title — reflect a player operating at a consistently high level. The win vaulted him to a career-high No. 3 in the Official World Golf Rankings.</p>

            <h3>Scheffler's Back-to-Back Runner-Up Finishes</h3>
            <p>One week after finishing second at the Masters, Scottie Scheffler was second again at the RBC Heritage. His final-round 67 — three birdies in a row on the back nine to force the playoff — was a superb performance by any measure, and his playoff appearance was earned rather than lucky. But Fitzpatrick produced a better shot at the decisive moment, and that was the difference. Two consecutive runner-up finishes for the world's No. 1 player says more about the quality of the competition in 2026 than it does about any weakness in Scheffler's game.</p>

            <h3>Fitzpatrick on the Crowd</h3>
            <p>After the playoff, Fitzpatrick was asked about the partisan atmosphere. His answer was worth noting: "No one was shouting on backswings or anything like that, which was great. I'm all for it. I love the people. They're supporting Scottie. You want golf to have an atmosphere. Having them chanting at you every week is a great feeling. However, there's no better feeling than coming out on top against that." It was the response of a player who has learned to draw energy from the pressure rather than buckle under it.</p>
        </section>

