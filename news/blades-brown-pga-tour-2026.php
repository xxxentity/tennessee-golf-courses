    <div class="article-content">
        <div class="article-intro">
            <p>Four days after turning 19, Blades Brown earned Special Temporary Membership on the PGA Tour. The Nashville native finished tied for 14th at the CJ Cup Byron Nelson on May 24, crossing the threshold that gives him access to unlimited sponsor exemptions for the rest of the 2026 season. It was the latest milestone in a run of professional results that has made the teenager one of the most talked-about young players in golf — and Tennessee's most exciting story on the national stage.</p>
        </div>

        <section>
            <h2>Who Is Blades Brown</h2>
            <p>Brown was born May 21, 2007, in Nashville. His mother, Rhonda Blades Brown, played basketball at Vanderbilt and went on to play professionally in the WNBA — she was the first player in WNBA history to score a three-point shot. His surname comes from her maiden name. His father, Parke Brown, is a Nashville businessman who introduced Blades to golf at Richland Country Club when he was eight years old.</p>

            <p>By 16, Brown was already drawing national attention. At the 2023 U.S. Amateur at Colorado Golf Club, he shot a course-record 64 in the stroke play portion of the championship, breaking Bobby Jones' 103-year-old record as the youngest medalist in U.S. Amateur stroke play history. Jones set that mark in 1920 at age 18. Brown was a high school sophomore.</p>

            <p>Despite a string of college offers, Brown turned professional on December 17, 2024, at 17 years old, forgoing college entirely. He had barely graduated high school.</p>
        </section>

        <section>
            <h2>The Course-Record 60 at the American Express</h2>
            <p>Brown's first full PGA Tour start of 2026 came at the American Express in La Quinta, California, in January. In the second round, playing the Nicklaus Tournament Course at PGA West, he shot a bogey-free 60 — setting a new course record. He made six birdies and an eagle in his first seven holes alone, going out in 28. On the back nine he birdied four of his first six holes. Standing on the final green needing a birdie for 59, he missed a 15-foot putt that stayed right. The 60 still stood as the lowest round of the tournament, and it made Brown the youngest player in PGA Tour history to shoot 60 or better, breaking a mark previously held by Patrick Cantlay (60 at the 2011 Travelers Championship).</p>

            <p>Through two rounds, Brown was tied with Scottie Scheffler at 17-under — co-leading the event alongside the world's top-ranked player. He faded over the weekend but still finished the week with his most complete PGA Tour performance to that point.</p>
        </section>

        <section>
            <h2>Almost the Youngest Winner in 95 Years at the Puerto Rico Open</h2>
            <p>In March 2026, Brown entered the Puerto Rico Open and put himself in position to make history again. He held a one-shot lead with six holes to play on Sunday, needing to hold on to become the youngest PGA Tour winner in 95 years. He didn't. On one hole, his ball went from a greenside bunker into the water, resulting in a triple bogey that ended his chance at the title. He finished third. Ricky Castillo won the event. The collapse was a painful reminder of how fine the margins are at this level, but finishing third while leading a PGA Tour event with six holes to play is not the result of a player who doesn't belong.</p>
        </section>

        <section>
            <h2>Korn Ferry Tour and the Path to Membership</h2>
            <p>Brown spent much of the 2026 spring on the Korn Ferry Tour, where he has been equally hard to ignore. He led the 54-hole standings at both the Club Car Championship at The Landings and the Tulum Championship at PGA Riviera Maya before failing to convert either into a win, but his consistency across both tours has been the story. He is currently 13th on the Korn Ferry Tour season-long points list — a position that, if it holds through the end of the season, would earn him a full PGA Tour card for 2027.</p>

            <p>The CJ Cup Byron Nelson in May moved him even closer to a decision point. Brown turned 19 during the first round of the tournament — on May 21, the same birthday that saw him begin his fourth year of life that has included a U.S. Amateur record, a Korn Ferry Tour history, and a course-record 60 on the PGA Tour. He closed with a 67 to finish T14 at 18-under, surpassing the FedExCup points threshold that earns him Special Temporary Membership. He said after the tournament that he plans to continue on the Korn Ferry Tour and chase the full card for 2027, with the Korn Ferry Tour's top 20 at season's end earning PGA Tour status. He has also retained the ability to accept unlimited PGA Tour sponsor exemptions through the remainder of 2026 if he chooses.</p>
        </section>

        <section>
            <h2>What It Means for Tennessee Golf</h2>
            <p>Tennessee has produced its share of PGA Tour players, but the pace at which Brown has moved through the professional ranks — from U.S. Amateur record holder at 16 to PGA Tour course record holder at 18 to Special Temporary Member at 19 — is genuinely unprecedented. He is the youngest player in Korn Ferry Tour history to shoot 61, the youngest in PGA Tour history to shoot 60, and he nearly won a PGA Tour event in his second calendar year as a professional. Nashville's Richland Country Club, where his father first put a club in his hands at age eight, produced that.</p>
