        <div class="article-intro">
            <p>Two hundred and thirty-nine PGA Tour starts. Eight years removed from a car accident that collapsed his lung, broke his ribs, fractured his leg, and put him through surgeries he couldn't count on one hand. Two years back on Tour after a stretch away that had nothing to do with choice and everything to do with survival. Bud Cauley stood on the 18th green at TPC Toronto at Osprey Valley on Sunday afternoon, signed for a five-under 65, and waited to see if anyone could catch him. Nobody could. Cauley is the 2026 RBC Canadian Open champion — two shots clear of Matt Fitzpatrick at 17 under par — and the moment was as large as the journey that led to it.</p>
