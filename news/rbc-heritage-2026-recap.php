            <h2>Tournament Overview</h2>
            <p>The RBC Heritage, held April 16-19 at Harbour Town Golf Links in Hilton Head Island, South Carolina, is one of the most beloved Signature Events on the PGA Tour calendar. The iconic lighthouse behind the 18th green is one of professional golf's most recognizable backdrops, and the course's tight, tree-lined layout rewards precision over power in a way that makes it unique on a modern schedule that frequently favors length. Fitzpatrick, one of the best iron players in the world, has always been well-suited to this track — and 2026 proved no different.</p>

            <p>Fitzpatrick entered the final round with a three-shot lead, which by Harbour Town standards feels comfortable. By the back nine on Sunday, it felt like anything but.</p>
