            <h2>Tournament Overview</h2>
            <p>The 2026 Travelers Championship was played June 25 through 28 at TPC River Highlands in Cromwell, Connecticut, with the Monday playoff conclusion on June 29. The course, a par-70 layout situated along the Connecticut River, has hosted the Travelers Championship since 1984 and is one of the most scoring-friendly venues on the PGA Tour calendar — a track where low numbers are expected, birdie runs are common, and the tournament regularly produces some of the lowest winning scores of the season. The $20 million purse made it one of the higher-stakes regular-season events outside the majors, drawing a full Signature Event field that included the world's top players.</p>

            <p>The week did not go quietly. Scottie Scheffler nearly shot a 59 in the second round. A 90-minute weather delay disrupted the final round Sunday and ultimately made a Monday conclusion unavoidable. And Collin Morikawa, who entered Sunday outside the top three, played the best round of the week — a nine-under 61 — to nearly gate-crash the entire thing. By the time Hovland and Scheffler walked to the 18th tee on Monday morning for the playoff, the tournament had already delivered more than enough for any single week.</p>
