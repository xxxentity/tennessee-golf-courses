        <section>
            <h2>How It Unfolded</h2>
            <p>Bridgeman's first three rounds at Riviera were as good as anyone has played the course in years — a 66, a 66, and then a 64 on Saturday that stretched his lead to six shots entering Sunday. At seven-under through 12 holes in the final round, the lead was briefly seven. Then the wheels began to wobble.</p>

            <p>From the 12th hole onward, Bridgeman made nothing. Eight straight pars down the stretch while the leaderboard lit up behind him. Adam Scott posted a dazzling 63 to climb to 16-under. Kurt Kitayama fired a 64 to get within one. Rory McIlroy shot a 67 to tie Kitayama at 17-under and make it a genuine battle with the leader still on the course.</p>

            <p>By the time Bridgeman reached the 18th tee, his six-shot cushion had been shaved to one. He made par on the closing hole, but not without drama — a 3-foot par putt stood between him and a playoff, and he made it. The closing 72 was 1-over on a day when the course yielded low scores, but the number next to his name on the leaderboard was what mattered: -18, alone at the top, winner by one.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>Bridgeman's First Win</h3>
            <p>Jacob Bridgeman is a 26-year-old from South Carolina who came up through the Korn Ferry Tour after a standout career at Clemson. This was his 66th PGA Tour start — a milestone that took patience and perseverance to reach in a sport where some players wait far longer for a breakthrough, and others never get one. The win at Riviera made him the first player to win the Genesis Invitational in his tournament debut since Pat Fitzsimons accomplished the same feat in 1975. He did it the hard way, grinding out the final holes under immense pressure with his lead shrinking by the minute.</p>

            <h3>Scott and Kitayama's Sunday Charges</h3>
            <p>The closing charges from Adam Scott and Kurt Kitayama deserve recognition. Scott's 63 was the low round of the tournament and a reminder of how well the 45-year-old Australian can still play when he is dialed in on a classic course like Riviera. Kitayama's 64 was equally impressive. Both players gave Bridgeman everything he could handle on Sunday — the fact that it wasn't enough speaks to Bridgeman's nerve on the final hole when everything was on the line.</p>

            <h3>McIlroy Comes Up One Short</h3>
            <p>Rory McIlroy, coming in as the defending Masters champion, put himself in position with a solid 67 on Sunday to finish tied for second at 17-under. A one-shot loss at Riviera stings, but McIlroy's form through the early weeks of 2026 has been consistently strong, and the major season is still months away.</p>
        </section>

        <section>
            <h2>Final Thoughts</h2>
            <p>The 2026 Genesis Invitational will be remembered as much for Sunday's drama as for Bridgeman's first three rounds of brilliance. A six-shot lead that shrank to one, a leaderboard full of world-class players making runs, and a 3-footer on the 18th to seal it — that is the kind of finish Riviera is built for. Bridgeman earned every bit of it, and his ability to make that final putt when his hands had gone cold on the putter for most of the back nine says something real about his composure. He left Pacific Palisades as a PGA Tour champion, and that part doesn't come with an asterisk.</p>
