            <h2>How It Unfolded</h2>

            <h3>Saturday: A Leaderboard Nobody Could Predict</h3>
            <p>Alex Smalley, ranked 78th in the world, entered Sunday as the 54-hole leader at six under par — and he had gotten there in thoroughly dramatic fashion. He made three bogeys in his first four holes of Saturday's third round, effectively dropping out of contention before anyone had played a dozen holes. Then he steadied himself, birdied the par-5 ninth to get the momentum moving, and played the final ten holes in six under par: six birdies, one bogey. It was one of the gutsiest back-nine performances of the week and vaulted him to the top of the most crowded leaderboard in PGA Championship history.</p>

            <p>Rahm, McIlroy, Åberg, Matti Schmid, Nick Taylor, and Aaron Rai were all within two shots heading into Sunday. Scheffler, the two-time PGA Champion, was five back. "Anyone can win this," Scheffler said after Saturday's round, and he meant it.</p>

            <h3>Sunday: Rai Takes the Back Nine Apart</h3>
            <p>Rai began the day two behind Smalley. He made the turn at one under, but it was the back nine where the championship was decided. He birdied the par-4 11th to inch closer, then arrived at the par-4 13th — 299 yards, drivable for the longest hitters — with a delicate bunker shot from 40 yards that he absolutely had to execute. He had options: play safe, leave it uphill, accept a possible bogey. He didn't play safe. He flew the ball directly at the flag, used a slope falling away from the hole behind the pin, and watched the ball settle close enough to convert for birdie. Two-shot lead. It was the shot of the week.</p>

            <p>He birdied the par-5 16th with another clean approach to give himself breathing room, and then on the 17th green he holed a winding, 69-foot putt for birdie that dropped the entire leaderboard three shots below him in one stroke. The roar that followed echoed across the west Philadelphia suburbs. When Rai walked off the 18th green, he had shot 65 — four under on the back nine alone — and won the Wanamaker Trophy by three shots over Rahm and the faltering Smalley.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>The First English Winner in More Than a Century</h3>
            <p>Jim Barnes won the first two PGA Championships, in 1916 and 1919, as an English-born golfer who had emigrated to the United States. No Englishman had won it since. Over a century's worth of Ryder Cup heroes, major contenders, and world number ones from England came and went without anyone claiming the Wanamaker Trophy. Rai put his name in that history simply by going out on a Sunday afternoon at Aronimink and playing the round of his life when it mattered most.</p>

            <h3>The First Player of Indian Descent to Win a Men's Major</h3>
            <p>Rai's father Amrik is of Indian heritage, making Aaron the first player of Indian descent to win a men's major championship in the history of the game. That fact landed with particular resonance — golf is a global sport, but the history of major championship winners has been narrow in its geography and demographics. Rai represents something genuinely new. The stories that have already been written about what this win means to communities of Indian descent in England and around the world will outlast the scorecard.</p>

            <h3>The Quirks That Became the Story</h3>
            <p>Every major champion eventually gets a defining image, and for Rai it will be the two gloves — one on each hand, as it has been since he was eight years old and playing cold, wet rounds in the English Midlands, unwilling to lose his grip on a club he couldn't yet fully control. He also uses iron covers on all his irons, a habit instilled by his father, who gave him his first expensive set of Titleist irons as a boy and taught him to clean every groove afterward with a pin and baby oil. "To protect the golf clubs, he thought it would be good to put iron covers on them," Rai said, "and I've pretty much had iron covers on all my sets ever since, just to kind of appreciate the value of what I have." On Sunday at Aronimink, those same clubs — covered between shots, protected since childhood — won a major championship.</p>

            <h3>Smalley Couldn't Hold On; Rahm Came Closest</h3>
            <p>Alex Smalley's final-round 70 — even par, the same score he would have signed for on any ordinary week — wasn't enough. He had led entering the day and couldn't generate the birdies he needed as the Aronimink greens got quicker and the pressure got heavier. He finished in a tie for second with Rahm at six under, a creditable result for a player ranked 78th in the world, and another agonizing near-miss for a player who has been knocking on the door of a breakthrough result for months. Rahm's closing 68 was the most competitive response in the field, but three shots is a large gap to overcome on a back nine as firm as Aronimink's.</p>

            <h3>A Course Built for Exactly This Kind of Player</h3>
            <p>Aronimink was last used for a major in 1962, and its Donald Ross design rewards a skill set that doesn't always show up at the top of modern PGA Tour leaderboards: precise iron play, disciplined course management, and the ability to hit greens in regulation from the fairway — because getting to the fairway is the prerequisite for everything else. Rai, the 44th-ranked player in the world, is exactly the kind of precision ballstriker this course was built to expose. He hit it where he needed it, made the putts when they mattered, and played Aronimink better than anyone in the field.</p>
        </section>

        <section>
            <h2>Final Thoughts</h2>
            <p>The 2026 PGA Championship at Aronimink Golf Club produced everything a major championship should deliver: a historic venue hosting its first major in 64 years, an impossibly crowded leaderboard, a final round where nearly two dozen players had reason to believe they could win, and at the center of it all, an unheralded 31-year-old from Wolverhampton with two gloves and iron covers who played the back nine of a major Sunday better than anyone in a generation. Aaron Rai is the 2026 PGA Champion. He is the first Englishman to win it since 1919. He is the first player of Indian descent to win a men's major. And he got there by hitting a bunker shot on the 13th and sinking a 69-footer on the 17th on the biggest Sunday of his life. Golf produces stories like this very rarely. This was one of them.</p>
