        <div class="article-intro">
            <p>Lucas Herbert was sick enough on Wednesday that he managed only nine holes of pre-tournament preparation before calling it a day. He woke up Sunday morning feeling, by his own account, worse than he had all week. In between, he shot 64 and 63 to build an eight-shot lead at Trump National Golf Club and then held off Sergio Garcia's back-nine charge to win the MAADEN LIV Golf Virginia by four shots. You do not need to feel well to play like this.</p>
        </div>

        <section>
            <h2>Tournament Overview</h2>
            <p>The MAADEN LIV Golf Virginia, held May 7-10 at Trump National Golf Club in Potomac Falls, Virginia, marked LIV Golf's return to the Washington D.C. area after a two-year absence. The par-72 layout stretching to 7,679 yards is one of the circuit's more scenic American venues — riverfront views and significant elevation changes give the course a character that suits the league's entertainment-first model. Herbert, a 30-year-old Australian and member of the all-Aussie Ripper GC squad, looked like a player who had been preparing for a course like this his entire career, even as the flu quietly tried to derail the effort.</p>

            <p>The event was the fifth of the 2026 LIV Golf season and the fourth individual title of the year to go to a first-time LIV winner, extending a theme that has defined the circuit's 2026 calendar.</p>
        </section>

