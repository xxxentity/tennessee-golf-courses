            <h2>Tournament Overview</h2>
            <p>The American Express, held January 22-25 at PGA West in La Quinta, California, rotates players across three courses — the Stadium Course, the Nicklaus Tournament Course, and La Quinta Country Club — before a final-round showdown on the Stadium layout, home to some of the most recognizable holes in desert golf. Chief among them is the par-3 17th, nicknamed "Alcatraz" for its island green surrounded by water, which has swallowed more than a few tournament dreams over the years.</p>

            <p>Scheffler arrived in the California desert as the heavy favorite and played like it from the opening round. An opening 63 set the tone, and he never relinquished a position near the top of the leaderboard through all four days.</p>
