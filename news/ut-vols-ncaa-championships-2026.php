    <div class="article-content">
        <div class="article-intro">
            <p>The No. 18 Tennessee men's golf team is competing at the 2026 NCAA Division I Men's Golf Championship at Omni La Costa Resort & Spa in Carlsbad, California. Play began Friday, May 29, and runs through Wednesday, June 3. It is Tennessee's third consecutive appearance at the national championship — and the program's eighth straight NCAA Regional berth. Stroke play runs May 29-31, with the top 15 teams advancing to a fourth round on June 1 before an eight-team match play bracket settles the national champion on June 2-3.</p>
        </div>

        <section>
            <h2>How Tennessee Got Here: The Bryan Regional</h2>
            <p>Tennessee was selected to the Bryan Regional at Traditions Club in Bryan, Texas, seeded third in the field behind Texas and North Carolina. What followed was one of the more dramatic regional finishes of the spring.</p>

            <p>Weather compressed the final two rounds of the tournament into a single day. Tennessee entered that Tuesday in fifth place — but slipped to sixth, dropping into double-digit deficit territory before a rally late in the second round carried momentum into the third. Seniors Josh Hill and Lance Simpson took control in the final stretch, Hill carding a 4-under round and Simpson firing a 5-under to drive the comeback. Redshirt junior Bruce Murphy closed with a 71. Tennessee finished at 18-under par, good for fifth place and five strokes clear of sixth-place TCU — enough to punch their ticket to California.</p>

            <p>It was the program's eighth consecutive NCAA Regional appearance under head coach Brennan Webb, who enters his eighth season at Tennessee. Webb has led the Vols to back-to-back top-20 finishes at the national championship in 2024 and 2025 — the first time Tennessee had done that in back-to-back years since 1980 and 1981.</p>
        </section>

        <section>
            <h2>Players to Watch</h2>
            <p>The most prominent name on Tennessee's lineup heading into La Costa is sophomore Jackson Herrington of Dickson, Tennessee. Herrington tied for low amateur honors at the 2026 Masters Tournament at Augusta National — a performance that established him as one of the better college players in the country. During the 2025-26 season he has posted 15 rounds of par or better and recorded two top-10 individual finishes. He will be the focal point of how far Tennessee can go in match play if the team advances through stroke play.</p>

            <p>Freshman Chase Kyes has been one of the steadier contributors across the lineup in his first college season, posting 18 rounds of par or better and delivering two qualifying rounds at the Bryan Regional when it mattered most. Murphy brings three spring top-10s and reliable ball-striking. Hill and Simpson, the two seniors, provided the rounds that got Tennessee out of Texas — and the national stage of their final season as Vols is now in front of them.</p>
        </section>

        <section>
            <h2>The Field and Format</h2>
            <p>Thirty teams and six individuals are competing in stroke play at La Costa's Champions Course, a par-72 layout stretching 7,528 yards designed by Gil Hanse. Omni La Costa has hosted the NCAA Championships for the third consecutive year. Auburn arrived as the No. 1 seed; Oklahoma State is the defending national champion.</p>

            <p>Tennessee is one of 11 programs to have qualified for the national championship every year the tournament has been held at La Costa. Three rounds of stroke play determine which 15 teams and nine individuals not on an advancing team move to a fourth stroke play round on June 1, which crowns the individual champion and sets the eight-team match play bracket. Quarterfinals and semifinals are June 2; the national championship match is June 3.</p>
        </section>

        <section>
            <h2>What a Third Straight Appearance Means</h2>
            <p>Consecutive national championship appearances were not a given for this program when Webb took over. He has now taken Tennessee to three straight, back-to-back top-20 national finishes, and is in the process of building the program's best sustained run of postseason success in more than four decades. The incoming 2026 recruiting class — ranked third in the country, headlined by the top-ranked junior in the world in Tyler Watts and top-10 recruit Pennson Badgett — suggests the program's trajectory is not leveling off. The current group's run at La Costa is a chapter in a longer story.</p>
        </section>
