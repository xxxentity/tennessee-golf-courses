        <section>
            <h2>How It Unfolded</h2>

            <h3>Two Rounds, Eight Shots Clear</h3>
            <p>Herbert's opening 64 set the tone and drew little notice — big first-round scores at LIV events are common, and there was no particular reason to think this would sustain. Then he went out Thursday and shot 63. Two rounds in, he stood at 17-under with an eight-shot cushion over the field. It was a remarkable display of ballstriking from a player who was dragging himself to the course each morning on medication and willpower.</p>

            <p>He was asked after round two about the illness. His response was simple: he wasn't going to stop unless someone made him. No one did.</p>

            <h3>Garcia Makes His Move</h3>
            <p>By the time Sunday arrived, Herbert led Garcia by three shots heading into the final round. Garcia, 46 years old and playing some of the most consistent golf of his 2026 LIV season, came out firing. He reached the back nine having closed the gap to a single stroke — his driver and putter both working with a precision that gave the impression Herbert's lead might vanish entirely. It was the kind of Sunday charge that has defined Garcia's career at its best.</p>

            <p>Herbert answered. He made the birdie he needed to push the margin back out, and two holes later at the par-5 12th he birdied again while Garcia could only make par. The swing in momentum was decisive. Herbert navigated a brief rain delay with two holes remaining and finished his round of 69 for a four-shot victory that felt more comfortable by the end than the Sunday leaderboard had ever promised.</p>
        </section>

        <section>
            <h2>Key Storylines</h2>

            <h3>A First LIV Title, Earned the Hard Way</h3>
            <p>Herbert joined LIV Golf in 2024 as part of Cameron Smith's Ripper GC team, bringing a résumé that included wins on the DP World Tour and a PGA Tour title at the 2021 Bermuda Championship. He had been competitive without converting in his time on the circuit, and the Virginia win felt overdue in the way that first wins for established players often do. Doing it wire-to-wire while physically fighting through illness gave the result an additional layer of substance that a comfortable clean win wouldn't have carried.</p>

            <h3>Garcia's Elite Week Goes Unrewarded</h3>
            <p>There was no shortage of performance to admire from Sergio Garcia. The 46-year-old Spaniard led the field in Strokes Gained: Putting for the week — over seven strokes gained against the field with the flat stick — and also ranked fifth off the tee. His rounds of 66-67-65-70 represented one of the more complete individual weeks of his 2026 LIV season, and on another week it would have been a winning performance. Herbert simply played better. Garcia settled for second at 20-under and moved up the individual standings as a result.</p>

            <h3>Herbert Books a U.S. Open Berth</h3>
            <p>Beyond the trophy and the $4 million first-place check, Herbert's win carried a consequence that mattered as much as any result of the week: he earned an exemption into the U.S. Open at Shinnecock Hills. It would be his first U.S. Open appearance in three years, and his form going in — a wire-to-wire 24-under victory in his most recent start — makes him one of the more intriguing names on any future major leaderboard.</p>

            <h3>4Aces GC Win the Team Competition in a Playoff</h3>
            <p>The team competition produced its own drama. Dustin Johnson's 4Aces GC and Sergio Garcia's Fireballs GC both finished regulation at 49-under, forcing a playoff on the 18th hole. Neither captain competed — Johnson sent Anthony Kim and Thomas Detry, while Garcia went with the younger tandem of Josele Ballester and David Puig. Kim had shot an extraordinary 62 on Sunday to pull 4Aces level, and the team's playoff pair made par on 18 to win when Puig made bogey. It was 4Aces' third team title of the 2026 season and moved them to the top of the team standings.</p>

            <p>The subplot here was Kim. The 62 he carded on Sunday — bogey-free, 10-under — was an extraordinary number and kept 4Aces' title run alive when it looked like it might slip away. His 2026 season, following the comeback win at Adelaide earlier in the year, has been one of the more compelling storylines in professional golf.</p>
        </section>

        <section>
            <h2>Final Thoughts</h2>
            <p>The MAADEN LIV Golf Virginia gave the circuit a wire-to-wire champion who spent the week fighting the flu, a veteran runner-up who played his best golf of the year and still came up four short, and a team playoff that needed Anthony Kim shooting 62 just to keep the title race alive. Herbert's win was the kind of performance that earns the respect of the locker room — not flashy, not lucky, just relentlessly good over four days against a field that had every opportunity to chase him down. The U.S. Open field got a serious contender. LIV Golf got its story of the week.</p>
