            <h2>Tournament Overview</h2>
            <p>The 51st Memorial Tournament presented by Workday unfolded at Muirfield Village Golf Club in Dublin, Ohio from June 4 through June 7, 2026. Jack Nicklaus's course has hosted this PGA Tour Signature Event since 1976, and this edition was scheduled to be a clean four-day affair — until two thunderstorms rolled through Dublin on Saturday and turned the third round into a weather casualty. The round was suspended twice, with play ultimately called for the day at 4:34 p.m. ET, leaving the entire field with unfinished business and a brutal Sunday ahead.</p>

            <p>What followed was a 33-hole marathon: 13 holes to complete the third round in the morning, then a full fourth round in the afternoon, then two playoff holes as evening crept in. The $20 million purse — standard for a Signature Event — and the prestige of Nicklaus's personal tournament had the best players in the world at Muirfield Village. Not all of them showed it. The one who did was the one nobody expected.</p>
