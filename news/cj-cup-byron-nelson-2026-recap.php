            <h2>Tournament Overview</h2>
            <p>The CJ Cup Byron Nelson, held May 21-24 at TPC Craig Ranch in McKinney, Texas, is one of the more scoring-friendly events on the PGA Tour calendar, and the 2026 edition played true to form. Kim went low early and often, Scheffler joined the top of the board through the weekend, and by Saturday night three players were separated by two shots with the whole field seemingly capable of putting up a number on Sunday. Clark was tied with Scheffler, two behind Kim, when the final round began. He solved the day faster than anyone could have anticipated.</p>
