    <div class="article-content">
        <div class="article-intro">
            <p>The Honors Course in Ooltewah, Tennessee will host the 126th U.S. Women's Amateur Championship from August 4-9, 2026. It marks the seventh USGA championship played at the private club near Chattanooga — and the first U.S. Women's Amateur in Tennessee since the championship began in 1895. When the week is over, the Honors Course will become only the 26th venue in history to have hosted both a U.S. Amateur and a U.S. Women's Amateur.</p>
        </div>

        <section>
            <h2>About The Honors Course</h2>
            <p>The Honors Course was founded by Jack Lupton, a Chattanooga businessman and Coca-Cola bottling heir, who broke ground on the property in 1983 with a specific purpose in mind: to build a private sanctuary dedicated to amateur golf, free from the social trappings of a traditional country club. He hired Pete Dye to design it. The result, situated at the foot of White Oak Mountain near Chattanooga, was regarded as genuinely radical at the time — tall native grass rough, Zoysia fairways that were unusual in the American South, and greens that played fast and uncompromisingly.</p>

            <p>The course has been ranked first in Tennessee since 1987 and sits among the top 30 courses in the United States on Golf Digest's most recent ranking. The layout stretches 7,500 yards from the back tees — up from its original 7,064 yards — at par 72.</p>

            <p>In 2020, a tornado swept through the property and caused significant damage, prompting a renovation led by Gil Hanse. Hanse's work was intentionally minimal: restoring the 10th and 11th greens to their original Pete Dye shapes, repositioning bunkers to suit modern ball-striking distances, exposing a creek on the 18th hole that had been partially obscured, and transitioning the fairways to Zeon Zoysia for improved playability year-round. The course reopened and has been widely considered better for it.</p>
        </section>

        <section>
            <h2>The Honors Course's USGA Championship History</h2>
            <p>The 2026 U.S. Women's Amateur will be the seventh USGA championship held at the Honors Course. The club's relationship with championship golf began early and has continued across four decades.</p>

            <p>The first was the 1991 U.S. Amateur, won by Mitch Voges, who defeated South Africa's Manny Zerman 7 and 6 in the 36-hole final. Voges, 41 at the time, became the third-oldest winner in U.S. Amateur history, and his win at the Honors Course came in the same era as Phil Mickelson's victory in 1990 and Justin Leonard's in 1992. The 1994 Curtis Cup followed, with the United States defeating Great Britain and Ireland. In 2005, the U.S. Mid-Amateur was contested there, won by Kevin Marsh. The 2011 U.S. Senior Women's Amateur went to Terri Frohnmayer.</p>

            <p>Most recently, the 2016 U.S. Junior Amateur was played at the Honors Course, won by 17-year-old Min Woo Lee of Australia in a 2-and-1 final over Noah Goodwin of Texas. Lee became the first Australian to win the U.S. Junior Amateur and, together with his sister Minjee, formed the first brother-sister tandem to hold USGA junior titles simultaneously. He is now a PGA Tour winner.</p>

            <p>The Honors Course is also already on the books to host the 2031 U.S. Amateur — marking the 40th anniversary of Voges's 1991 win on the same grounds.</p>
        </section>

        <section>
            <h2>Format and Schedule</h2>
            <p>The USGA has adjusted the traditional weekly schedule for the 2026 U.S. Women's Amateur. Rather than the customary Monday-through-Sunday format, the championship will run Tuesday through Sunday — August 4-9. The change was made to give players who compete in the AIG Women's Open the week prior an additional day of travel time before arriving in Tennessee.</p>

            <p>Stroke play qualifying will determine the 64-match play qualifiers, who then play down to a 36-hole final on Sunday, August 9. The field includes the best amateur women's golfers in the world — a bracket that has historically included players who go on to significant professional careers.</p>
        </section>

        <section>
            <h2>What It Means for Tennessee</h2>
            <p>The U.S. Women's Amateur has been played 125 times since 1895. It has never been held in Tennessee. This August changes that.</p>

            <p>The Honors Course was built specifically to honor amateur golf. Hosting the most prestigious amateur women's championship in the United States — on a course ranked among the 30 best in the country, redesigned after a natural disaster, in a state that has never before hosted the event — is exactly the kind of moment the club was built for. Tennessee golf's national profile has been rising steadily, and a week in Ooltewah this August will put it on a stage it has not occupied before.</p>
