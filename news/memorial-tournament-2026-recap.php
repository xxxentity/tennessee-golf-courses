            <p>He came into the week without a single top-20 finish all season. He blew a four-shot lead on the final day of one of the most grueling Sundays in recent PGA Tour memory. And then, with the whole thing slipping away, J.T. Poston rolled in a seven-foot birdie on the 72nd hole to force a playoff — and won it two holes later when Ryan Gerard missed a six-footer to hand him the trophy. Poston is the 2026 Memorial Tournament champion. It was, by any measure, one of the stranger and more compelling weeks Muirfield Village has produced in years.</p>
