            <h2>Key Storylines</h2>

            <h3>Morikawa's Return to the Winner's Circle</h3>
            <p>The Pebble Beach title was Morikawa's seventh on the PGA Tour and his first since 2023. Known as one of the purest ball-strikers in the game, Morikawa had been knocking on the door without quite breaking through in recent months. The 18th hole shot — one of the hardest iron shots in professional golf when the coastal wind is in play — was a reminder of just how elite his ball-striking remains. When that part of his game is on, few players in the world are more dangerous.</p>

            <h3>Straka's Eagle Wasn't Enough</h3>
            <p>Sepp Straka's eagle on the 72nd hole was a moment that should have won — or at least forced a playoff. In any other week, holing out for eagle on 18 at Pebble Beach to take the lead in the final moments would be the story everyone was talking about. Instead, Morikawa's response made it a footnote. Straka finished tied for second alongside Min Woo Lee, another strong result for the Austrian that keeps him in the conversation as one of the more consistent performers on Tour.</p>

            <h3>Scheffler's 63 Arrives Too Late</h3>
            <p>A final-round 63 from Scottie Scheffler was the kind of round that wins most tournaments. At Pebble Beach this week, it wasn't enough. Scheffler moved up the board in spectacular fashion but finished two shots short in a share of fourth. It was a reminder that even when the world's No. 1 is at his best on Sunday, the competition in 2026 is good enough to match him.</p>
        </section>

