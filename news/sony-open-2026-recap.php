            <h3>Three Straight Years With a Title</h3>
            <p>The stat that stands out most from Gotterup's Sony Open win is the streak it extends: he has now won at least one PGA Tour event in three consecutive calendar years. For a player still in his mid-20s, that kind of consistency is a real marker of elite-level performance. He jumped to No. 17 in the world rankings following the win and enters the rest of the 2026 season as one of the more dangerous players in the game.</p>

            <h3>Riley's Difficult Sunday</h3>
            <p>Davis Riley came to Hawaii looking for a breakthrough win and came agonizingly close to putting himself in position for one. His final round was a lesson in how quickly things can unravel on a tight, breezy layout like Waialae. The back-to-back three-putts on six and seven were the turning point — one bogey is recoverable, two in a row against a surging leaderboard is a much steeper hill. Riley ultimately finished in a share of sixth, a disappointing result given where he stood after 54 holes.</p>

            <h3>MacIntyre's Sunday 63</h3>
            <p>Robert MacIntyre fired the low round of the day — a 63 — to share fourth at 12-under. It was the kind of closing round that will have him among the early favorites in the weeks ahead, a reminder of just how dangerous the Scotsman can be when his game clicks in the final round.</p>
