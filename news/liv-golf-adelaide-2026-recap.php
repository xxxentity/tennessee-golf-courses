        <section>
            <h2>How It Unfolded</h2>
            <p>Entering the final round, Kim sat five shots behind co-leaders Jon Rahm and Bryson DeChambeau — a deficit that would have felt insurmountable for most players in that situation. Kim didn't treat it that way. He went out and played the round of the week, a 9-under 63 that put steady pressure on the leaders and gradually turned a comfortable gap into a crisis.</p>

            <p>Rahm and DeChambeau could not sustain their form from the first three rounds. As Kim made birdie after birdie, the leaderboard shifted dramatically. By the time it was over, Kim stood alone at 23-under — three shots clear of Rahm in second and six clear of a three-way tie for third that included DeChambeau, Peter Uihlein, and Tyrrell Hatton. The final margin made it look almost routine. It was anything but.</p>
        </section>

        <section>
            <h2>The Story Behind the Win</h2>

            <h3>Sixteen Years in the Wilderness</h3>
            <p>Anthony Kim's last professional victory came in 2010 on the PGA Tour. What followed was more than a decade of injuries, personal struggles, addiction, and what appeared to many to be a career simply over before its time. He was 25 when he last won. He had the talent, the game, and the charisma to become one of the defining players of his generation. Instead, he disappeared.</p>

            <p>Kim returned to competitive golf in 2024 by joining LIV Golf. The return was not a fairytale from the start — in 14 starts across LIV Golf and the Asian Tour during his comeback season, he never finished inside the top 30. In 2025, he failed to earn a meaningful point in the season-long individual race and finished outside the top 40 in 12 of his 13 starts. There was nothing in the results to suggest this was coming.</p>

            <h3>The Win Nobody Saw Coming</h3>
            <p>That is exactly what made Adelaide so staggering. Kim didn't just scrape over the line — he fired the lowest round of the tournament on the final day to come from five back and win by three. At 40 years old, after everything he had been through, he stood on the 18th green at The Grange Golf Club in Australia with a trophy in his hands. The reaction across the sports world was immediate and universal: this was one of the great comeback stories golf has ever told.</p>

            <h3>Ripper GC Wins at Home</h3>
            <p>The team competition gave Australian fans an additional reason to celebrate. Ripper GC, led by Cameron Smith and now bolstered by Kim's individual title, won the team trophy at -55, claiming the victory on home soil in front of a crowd that had been pulling for them all week.</p>
        </section>

