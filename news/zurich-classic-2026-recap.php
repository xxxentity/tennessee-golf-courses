            <h2>Tournament Overview</h2>
            <p>The Zurich Classic of New Orleans, held April 23-26 at TPC Louisiana, is the only team event on the PGA Tour calendar and one of the most entertaining weeks of the season as a result. Teams of two compete in alternating formats — fourballs (best ball) on Rounds 1 and 3, and foursomes (alternate shot) on Rounds 2 and 4. Sunday's alternate-shot format is the most demanding of the week, requiring seamless communication, complementary strengths, and nerves of steel down the stretch. For the Fitzpatrick brothers, it brought out the best of all three.</p>

            <p>The Sheffield siblings came in with obvious chemistry and a shared swing coach in Mike Walker, who has worked with Matt throughout his career and took on Alex during a mid-season rebuild last year. That common foundation showed all week at TPC Louisiana.</p>
