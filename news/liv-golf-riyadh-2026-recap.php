            <h2>How It Unfolded</h2>
            <p>Smylie and Peter Uihlein entered the final round tied for the lead, setting up a classic Sunday showdown. What neither player — nor most observers — anticipated was Jon Rahm firing the round of the tournament: a 9-under 63 that gave him the lead at points during the final day and put Smylie under enormous pressure.</p>

            <p>The young Australian responded with a composed 64 of his own, making enough birdies to stay one ahead of Rahm's late charge. When the final putts dropped, Smylie stood alone at 24-under, one clear of the LIV Golf Individual Championship holder. It was a remarkable performance for any player — let alone one making his LIV debut.</p>

            <p>Uihlein, who had shared the overnight lead, finished third at 21-under, three shots back of the winner.</p>
