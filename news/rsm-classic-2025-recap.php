            <h2>Tournament Overview</h2>
            <p>The RSM Classic, held November 20-23 at Sea Island Golf Club in Sea Island, Georgia, closed out the PGA Tour's fall schedule with its usual blend of low scoring and high stakes. Players compete across two courses — the par-70 Seaside Course and the par-72 Plantation Course — over four rounds, with Sunday's finale played entirely on the Seaside layout.</p>

            <p>The week carried extra weight as the final opportunity for players on the FedExCup bubble to secure their Tour cards for 2026. With the top 100 in the FedExCup standings earning full exemptions, every shot in the closing round had potential career consequences for dozens of players on the outside looking in.</p>
